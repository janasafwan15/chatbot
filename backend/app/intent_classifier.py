# ─────────────────────────────────────────────
# Classification Logic
# ─────────────────────────────────────────────

def classify_intent(text: str) -> IntentResult:
    """
    يصنّف سؤال المواطن ويرجع IntentResult.

    الخوارزمية:
      - كل intent له keywords + patterns
      - نحسب score بناءً على الكلمات والـ patterns المطابقة
      - الـ weight يضاعف التأثير (لـ intents حساسة كالطوارئ)
      - أعلى score يفوز
    """
    t = _norm(text)
    if not t:
        return IntentResult(
            intent_code="unknown", category="general",
            confidence=0.0, answer_mode="rag",
        )

    scores: dict[str, float] = {}

    for intent in _INTENTS:
        code = intent["code"]
        score = 0.0
        weight = intent.get("weight", 1)

        # keywords
        for kw in intent.get("keywords", []):
            kw_norm = _norm(kw)
            if kw_norm in t:
                # كلمة طويلة → تطابق أدق → وزن أكبر
                kw_weight = len(kw_norm.split()) * 0.5 + 0.5
                score += kw_weight

        # patterns (regex)
        for pattern in intent.get("patterns", []):
            if re.search(pattern, t):
                score += 1.5  # pattern أقوى من keyword

        if score > 0:
            scores[code] = score * weight

    if not scores:
        return IntentResult(
            intent_code="general_inquiry", category="general",
            confidence=0.2, answer_mode="rag",
        )

    best_code = max(scores, key=lambda c: scores[c])
    raw_score = scores[best_code]

    # نحوّل الـ raw_score لـ confidence (0.0–1.0) بطريقة بسيطة
    confidence = min(raw_score / 8.0, 1.0)

    # ابحث عن الـ intent object
    intent_obj = next((i for i in _INTENTS if i["code"] == best_code), None)
    if not intent_obj:
        return IntentResult(
            intent_code="general_inquiry", category="general",
            confidence=0.2, answer_mode="rag",
        )

    # لو فيه أكثر من intent بفارق صغير → confidence أقل
    sorted_scores = sorted(scores.values(), reverse=True)
    if len(sorted_scores) >= 2 and sorted_scores[0] - sorted_scores[1] < 1.0:
        confidence = max(confidence * 0.75, 0.15)

    return IntentResult(
        intent_code=best_code,
        category=intent_obj["category"],
        confidence=round(confidence, 3),
        answer_mode=intent_obj["answer_mode"],
        direct_answer=intent_obj.get("direct_answer"),
    )


