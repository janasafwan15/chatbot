def is_off_topic(text: str) -> bool:
    """
    يرجع True لو السؤال واضح إنه خارج نطاق كهرباء الخليل.
    """
    t = _norm(text)

    # لو ذكر كلمة كهربا-related → ليس off-topic
    if any(word in t for word in _OFF_TOPIC_ELECTRICITY_RESCUE):
        return False

    for p in _OFF_TOPIC_PATTERNS:
        if re.search(p, t):
            return True

    return False


