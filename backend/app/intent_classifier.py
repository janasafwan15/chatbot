# ─────────────────────────────────────────────
# Normalization (مطابق لـ rag_engine)
# ─────────────────────────────────────────────
_AR_DIACRITICS = re.compile(r"[\u0617-\u061A\u064B-\u0652]")
_AR_NUMS_MAP = str.maketrans("٠١٢٣٤٥٦٧٨٩", "0123456789")


# ─────────────────────────────────────────────
# تطبيع اللهجة الفلسطينية — قبل _norm
# ─────────────────────────────────────────────
# كلمات دارجة شائعة الاستخدام في الخليل وجنوب الضفة
_DIALECT_MAP: list[tuple[str, str]] = [
    # ماء بكل أشكاله الدارجة
    ("مي",      "ماء"),
    ("مايه",    "ماء"),
    ("ماية",    "ماء"),
    ("مويه",    "ماء"),
    ("موية",    "ماء"),
    ("الماي",   "الماء"),
    ("بالماي",  "بالماء"),
    # طبّ / وقع / ضاع
    ("طبت",     "وقع"),
    ("طاب",     "وقع"),
    ("اتهرب",   "وقع"),   # اتهرب = انسكب
    # بدل تالف / ضايع
    ("ضاع",     "فقدان"),
    ("ضايع",    "فقدان"),
    ("فقد",     "فقدان"),
    # بركة / حوض
    ("بركه",    "ماء"),
    ("بركة",    "ماء"),
    ("حوض",     "ماء"),
    # تلف / خراب
    ("خربان",   "تالف"),
    ("خربت",    "تلف"),
    ("خربان",   "تالف"),
    ("باظ",     "تلف"),
    ("باظت",    "تلف"),
]

def _apply_dialect(text: str) -> str:
    """يحوّل الكلمات الدارجة الفلسطينية لمكافئها الفصيح قبل التطبيع."""
    t = text
    for dialect, standard in _DIALECT_MAP:
        # استبدال الكلمة الدارجة كـ كلمة مستقلة (حدود الكلمة)
        t = re.sub(r"(?<!\w)" + re.escape(dialect) + r"(?!\w)", standard, t)
    return t


def _norm(text: str) -> str:
    t = (text or "").lower().strip()
    t = _apply_dialect(t)          # ← تطبيع اللهجة أولاً
    t = _AR_DIACRITICS.sub("", t)
    t = t.translate(_AR_NUMS_MAP)
    t = t.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
    t = t.replace("ى", "ي").replace("ة", "ه")
    t = t.replace("ؤ", "و").replace("ئ", "ي")
    t = re.sub(r"[^\w\s\u0600-\u06FF]", " ", t)
    t = re.sub(r"\s+", " ", t).strip()
    return t


