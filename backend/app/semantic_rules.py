# backend/app/semantic_rules.py
from __future__ import annotations

import re

def _norm(s: str) -> str:
    t = (s or "").strip().lower()
    t = t.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
    t = t.replace("ى", "ي").replace("ة", "ه")
    return t

# كلمات إذا ظهرت غالباً السؤال مش فاتورة (إلا إذا ذكر "فاتورة/حساب" صراحة)
BLOCK_BILL_OVERRIDE_TERMS = [
    "عداد", "مسبق", "مسبق الدفع", "شحن", "كرت", "بطاقه", "بطاقة",
    "رقم", "هاتف", "تلفون", "واتساب", "ايميل", "طوارئ", "مراكز الشحن", "مركز شحن"
]

# أنماط "فاتورة عالية/اعتراض" لكن بشروط واضحة (بدون خطف أي رقم)
BILL_HIGH_PATTERNS = [
    # فاتورة/حساب + صفة ارتفاع
    r"(فاتور(ت|تي|تك|ته)|فاتوره|فاتورة|الحساب).*(عالي|غالي|نار|محر[قك]ه|مرتف[عع]ه|كبير)",

    # غلط/زيادة على الفاتورة/الحساب
    r"(غلط|زيادة).*(بالفاتور(ه|ة)|بالحساب)",

    # مش مشغل اشي / مش ساكن + لازم يذكر فاتورة/حساب
    r"(مش\s*مشغل[هة]?\s*اشي|انا\s*مش\s*مشغل[هة]?\s*اشي|مش\s*ساكن|مش\s*ساكنه).*(فاتور(ه|ة)|حساب)",

    # "طلعلي" رقم + لازم يكون مع فاتورة/حساب
    r"(طلع(ت|لي|لنا)).*(فاتور(ه|ة)|حساب).*(\d+)",

    # رقم + شيكل لكن لازم يذكر فاتورة/حساب
    r"(فاتور(ه|ة)|حساب).*(\d+)\s*(شيكل|₪)",
]

def semantic_intent_override(user_text: str) -> str | None:
    """
    ترجع intent جاهز إذا انمسكت حالة واضحة.
    حالياً: complaint_bill فقط، بس مع حمايات من الأرقام/الشحن/التواصل.
    """
    t = _norm(user_text)

    # إذا واضح إنه موضوع عداد/شحن/تواصل... لا تحوّليه لفاتورة
    if any(w in t for w in BLOCK_BILL_OVERRIDE_TERMS):
        if ("فاتور" not in t) and ("حساب" not in t):
            return None

    for p in BILL_HIGH_PATTERNS:
        if re.search(p, t):
            return "complaint_bill"
    return None

